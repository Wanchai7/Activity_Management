class User {
    private username:string;
    private password:string;
    private name:string;
    private role:string;
    private email:string;

    constructor(username:string , password:string , name:string , role:string , email:string){
        this.username = username;
        this.password = password;
        this.name = name;
        this. role = role;
        this.email = email;
    }
}